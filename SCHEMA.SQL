-- PURCHASE ORDER DATA
CREATE TABLE ORDER_DATA (
    ROW_NUMBER INT,
    PO_NUMBER INT,
    MATERIAL_NUMBER VARCHAR(30),
    PLANT_NUMBER VARCHAR(30),
    MOVEMENT_TYPE VARCHAR(30),
    POSTING_DATE DATE,
    VENDOR_ID VARCHAR(30),
    VENDOR_NAME VARCHAR(30),
    QUANTITY INT,
    PRIMARY KEY (ROW_NUMBER, MATERIAL_NUMBER)
);
-- INVENTORY DATA
CREATE TABLE INVENTORY_DATA (
    ROW_NUMBER INT,
    MATERIAL_NUMBER VARCHAR(30),
    PLANT_NUMBER VARCHAR(30),
    STORAGE_LOCATION VARCHAR(30),
    BLOCKED_INVENTORY INTEGER,
    HOLD_REASON VARCHAR(30),
    PRIMARY KEY (ROW_NUMBER, MATERIAL_NUMBER)
);
-- MATERIAL MASTER
CREATE TABLE MATERIAL_MASTER (
    MATERIAL_NUMBER VARCHAR(30),
    BUYER_CODE VARCHAR(30),
    PLANT_NUMBER VARCHAR(30),
    PRIMARY KEY (MATERIAL_NUMBER, PLANT_NUMBER)
);